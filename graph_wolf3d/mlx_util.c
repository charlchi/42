#include "wolf3d.h"

void	clear_img(int *img, int w, int h, int c)
{
	int				y;
	int				x;

	y = 0;
	while (y < h)
	{
		x = 0;
		while (x < w)
		{
			img[x + y * w] = c;
			x++;
		}
		y++;
	}
}
